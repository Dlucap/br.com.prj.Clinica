    public void preencherTabelaAgendamento(String sql) {

        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"ID", "NOME DO PACIENTE", "TEL.RESIDENCIAL", "TEL.CELULAR", "ACOMPANHANTE", "TEL ACOMPANHANTE", "CEL ACOMPANHANTE"};

        conBd.conectarBd();

        conBd.executaSql(sql);
        try {
            conBd.rs.first();
            do {
                dados.add(new Object[]{
                    conBd.rs.getInt("IDPACIENTE"), conBd.rs.getString("NOMEPACIENTE"), conBd.rs.getString("TELRESIDENCIAL"),
                    conBd.rs.getString("TELCELULAR"), conBd.rs.getString("CENOME"), conBd.rs.getString("CETELRESIDENCIAL"),
                    conBd.rs.getString("CETELCELULAR")}
                );
            } while (conBd.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao carregar os dados da tabela." + ex);
        }

        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jTableHorario.setModel(modelo);

    

        jTableHorario.getColumnModel().getColumn(1).setPreferredWidth(237);
        jTableHorario.getColumnModel().getColumn(1).setResizable(false);

        jTableHorario.getColumnModel().getColumn(2).setPreferredWidth(115);
        jTableHorario.getColumnModel().getColumn(2).setResizable(false);

        jTableHorario.getColumnModel().getColumn(3).setPreferredWidth(110);
        jTableHorario.getColumnModel().getColumn(3).setResizable(false);

        jTableHorario.getColumnModel().getColumn(4).setPreferredWidth(237);
        jTableHorario.getColumnModel().getColumn(4).setResizable(false);

        jTableHorario.getColumnModel().getColumn(5).setPreferredWidth(135);
        jTableHorario.getColumnModel().getColumn(5).setResizable(false);

        jTableHorario.getColumnModel().getColumn(6).setPreferredWidth(135);
        jTableHorario.getColumnModel().getColumn(6).setResizable(false);

        jTableHorario.getTableHeader().setReorderingAllowed(false);//reorganizar o cabeçalho
        jTableHorario.setAutoResizeMode(jTableHorario.AUTO_RESIZE_OFF); //NÃO VAI PODER REDIMENCIONAR A TABELA
        jTableHorario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);//selecionar um por vez 
        //total do tamanho dos campos = 38 + 237 + 110 + 65 = 450
        conBd.DesconectarBd();
    }

