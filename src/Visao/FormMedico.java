    public void preencherIdMedicoIncluir() {
        try {
            conBd.conectarBd();
            String sql = "SELECT MAX(IDMEDICO+1) AS PROXID FROM MEDICO (NOLOCK)";
            conBd.executaSql(sql);

            conBd.rs.first();

            jTextFieldIDMedico.setText(String.valueOf(conBd.rs.getInt("PROXID")));

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao gerar o proximo ID do médico! " + ex.getMessage());
        }
        conBd.DesconectarBd();
    }

    public void preencherEspecialidade() {
        conBd.conectarBd();

        try {

            jComboBoxEspecializacao.addItem("Selecione");
            String sql = "SELECT ESPEC FROM ESPECIALIDADE (NOLOCK) WHERE ATIVO = 1 ORDER BY ESPEC";

            pstM = conBd.con.prepareStatement(sql);
            rs = pstM.executeQuery();

            while (rs.next()) {
                jComboBoxEspecializacao.addItem(rs.getString("ESPEC"));
            }

        } catch (SQLException ex) {
            // Logger.getLogger(FormMedico.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher os dados da especialidade" + ex);
        } catch (NullPointerException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher os dados da especialidade" + ex.getMessage());
        }

        conBd.DesconectarBd();
    }

